package book.ch10.classes;

public class ClassInfo {
    private String name;
    private int number;

    public ClassInfo(String name, int number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}	
